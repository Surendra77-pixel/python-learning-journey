
#unpacking a list - Unpacking a list is a convenient way to assign the elements of a list to individual variables. It allows you to extract values from a list and assign them to separate variables in a single line of code. 

#example of unpacking a list:
fruits = ["apple", "banana", "cherry"]
a, b, c = fruits
print(a) #output: apple
print(b) #output: banana
print(c) #output: cherry
#explanation: In this code, we have a list of fruits with three elements. We use unpacking to assign each element of the list to a separate variable (a, b, and c). The first element "apple" is assigned to variable a, the second element "banana" is assigned to variable b, and the third element "cherry" is assigned to variable c. We then print the values of a, b, and c, which outputs "apple", "banana", and "cherry" respectively.


