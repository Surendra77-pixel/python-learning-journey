
#Datastructures in Python - They are used to store and organize data in a way that allows for efficient access and modification. Python provides several built-in data structures, including lists, tuples, sets, and dictionaries.

#1.list - A list is a collection of items that are ordered and changeable. It is defined using square brackets [] and can contain elements of different data types.
my_list = [1, "hello", 3.14, True]
print(my_list) #output: [1, 'hello', 3.14, True]

#2.tuple - A tuple is a collection of items that are ordered and unchangeable. It is defined using parentheses () and can contain elements of different data types.
my_tuple = (1, "world", 2.71, False)
print(my_tuple) #output: (1, 'world', 2.71, False)

#3.set - A set is a collection of unique items that are unordered and unindexed. It is defined using curly braces {} and can contain elements of different data types.
my_set = {1, "python", 3.14, True}
print(my_set) #output: {1, 'python', 3.14, True}

#4.dictionary - A dictionary is a collection of key-value pairs that are unordered and changeable. It is defined using curly braces {} and each key is separated from its value by a colon :.
my_dict = {"name": "surendra", "age": 25, "city": "hyderabad"}
print(my_dict) #output: {'name': 'surendra', 'age': 25, 'city': 'hyderabad'}

