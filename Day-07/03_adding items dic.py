
#Adding items to a dictionary
#1. using the key name -
my_dict = {
    "name": "Alice",
    "age": 30
}
my_dict["city"] = "New York"
print(my_dict) #output: {'name': 'Alice', 'age': 30, 'city': 'New York'} - A new key-value pair is added to the dictionary my_dict. The key is "city" and the value is "New York". When we print the dictionary, it reflects this addition, showing all three key-value pairs.  


