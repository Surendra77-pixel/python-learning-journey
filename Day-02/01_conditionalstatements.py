#conditiional statements : used to make decisions based on certain conditions

# if statement : used to execute a block of code if a condition is true

# if-else statement : used to execute a block of code if a condition is true and another block of code if the condition is false

# if-elif-else statement : used to execute a block of code if a condition is true, another block of code if another condition is true and another block of code if all conditions are false

#In conditional statements we had two types of execution :

#1.conditional execution : where a block of code is executed only if a condition is true
#2.repetitive execution : where a block of code is executed repeatedly as long as a condition is true

#Example of conditional execution
age = 18
if age >= 18:
    print("You are eligible to vote.")#output: You are eligible to vote.


#example of repetitive execution
count = 0
while count < 5:
    print(count) #output: 0 1 2 3 4
    count += 1

