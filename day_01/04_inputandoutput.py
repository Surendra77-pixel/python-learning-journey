#Input and output
#Input is the data that a program receives from the user or from another source, while output is the data that a program produces as a result of processing the input. In Python, you can use the built-in input() function to get input from the user and the print() function to display output to the console.
#Getting input from the user-

name=input("What is your name? ") # The input() function prompts the user to enter
print("Hello, " + name + "!") # Output: Hello, [name]!

age=input("What is your age? ") # The input() function prompts the user to enter their age
print("You are " + age + " years old.") # Output: You are [age] years old.

#outputting data to the console-
x=5
y=10
print("The value of x is:", x) # Output: The value of x is: 5
print("The value of y is:", y) # Output: The value of y is:10